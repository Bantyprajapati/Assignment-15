# Write a python script to create a String in 3 different possible ways.
# Method 1: Using double quotes
string1 = "Hello, World!"
print("Method 1:", string1)

# Method 2: Using single quotes
string2 = 'Hello, World!'
print("Method 2:", string2)

# Method 3: Using triple quotes for multiline strings
string3 = '''Hello,World!'''
print("Method 3:", string3)
